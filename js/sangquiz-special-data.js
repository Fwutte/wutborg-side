  function slugify(value) {
    return value
      .normalize("NFD")
      .replace(/[\u0300-\u036f]/g, "")
      .toLowerCase()
      .replace(/[^a-z0-9]+/g, "-")
      .replace(/^-|-$/g, "");
  }

  function spotifySearchUrl(title, artist) {
    return `https://open.spotify.com/search/${encodeURIComponent(`${title} ${artist}`)}`;
  }

  function makeSongs(edition, rows) {
    return rows.map(([title, artist, year, category, sourceTitle = "", sourceType = ""]) => ({
      id: `${edition}-${slugify(artist)}-${slugify(title)}`,
      title,
      artist,
      year,
      category: category === "da" ? "danish" : "international",
      edition,
      sourceTitle,
      sourceType,
      spotifyUri: "",
      spotifyUrl: spotifySearchUrl(title, artist),
      tags: [`${String(year).slice(2, 3)}0s`, category === "da" ? "da" : "international", edition],
    }));
  }

